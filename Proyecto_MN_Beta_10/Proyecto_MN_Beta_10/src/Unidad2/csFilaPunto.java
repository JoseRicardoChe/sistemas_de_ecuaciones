package Unidad2;

public class csFilaPunto {

    private int i; //Iteracion
    private double xo; //Inicial
    private double fxo; //funcion de xo F(Xo) conocido como Xi
    private double error; //Error Absoluto

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public double getXo() {
        return xo;
    }

    public void setXo(double xo) {
        this.xo = xo;
    }

    public double getFxo() {
        return fxo;
    }

    public void setFxo(double fxo) {
        this.fxo = fxo;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }

}
