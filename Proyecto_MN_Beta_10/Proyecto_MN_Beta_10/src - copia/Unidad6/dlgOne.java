    private void btCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCalcularActionPerformed
        try {
            double ay = Double.parseDouble(this.txtY0.getText().trim());
            double ax1 = Double.parseDouble(this.txtX1.getText().trim());
            double ax2 = Double.parseDouble(this.txtX2.getText().trim());
            int ai = Integer.parseInt(this.txtIteracion.getText().trim());
            csMetodoOne me = new csMetodoOne();
            ArrayList<csFilaOne> o = me.One(ay, ax1, ax2, ai);
            mtOne m = new mtOne(o);
            this.tbTablaDatos.setModel(m);
            //this.txtResultado.setText(String.valueOf(me.calcularValor(N,b,x)));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Numero invalido: " + e.getMessage());
        }
    }//GEN-LAST:event_btCalcularActionPerformed

