    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int f = Integer.parseInt(this.txtX.getText().trim());
            double N = Double.parseDouble(this.txtValor.getText().trim());     
            csMetodoNew me = new csMetodoNew();     
            ArrayList<csFilaNew> o = me.New(f);
            mtNew m = new mtNew(o);
            this.tbTablaDatos.setModel(m);

            double[] x =me.x;
            double[] fx=me.fx;
            double[] b = me.solucionar(x, fx);
            
            this.txtResultado.setText(String.valueOf(me.calcularValor(N,b,x)));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Numero invalido: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

